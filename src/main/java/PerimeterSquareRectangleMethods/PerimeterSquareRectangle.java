package PerimeterSquareRectangleMethods;

public class PerimeterSquareRectangle {

    public int perimeterOfSquare(int sideOneOfSquare){
        return sideOneOfSquare * 4;
    }

    public int perimeterOfRectangle(int sideOneOfRectangle, int sideTwoOfRectangle){
        return (sideOneOfRectangle + sideTwoOfRectangle) * 2;
    }
}
