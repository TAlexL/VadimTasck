package PackageFive;

public class ClassFiveAreaCircleVolumeSphere {

    public float areaOfCircleMethod(int r) {
        float f = (float) (3.14 * (r * r));
        return f;
    }

    public float volumeSphereMethod(int r) {
        float f = (float) (1.333 * 3.14 * (r * r * r));
        return f;
    }
}